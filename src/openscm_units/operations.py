from __future__ import annotations


def add_two(a: float, b: float) -> float:
    """
    Add two numbers

    Parameters
    ----------
    a
        First number

    b
        Second number

    Returns
    -------
    :
        Sum of the numbers

    Examples
    --------
    >>> add_two(1, 3)
    4
    >>> add_two(1, -3)
    -2
    """
    return a + b
